CREATE TABLE OrdersInfo(OrderNumber INT PRIMARY KEY, ProductID INT REFERENCES product(ProductID), CustomerID INT REFERENCES Customer(CustomerID), SellPrice DECIMAL, SellDate DATE, ShipDate DATE);
